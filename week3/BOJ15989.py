T = int(input())
dp = [0, 1, 2, 3, 4]
stand = 1
for i in range(5, 10001):
    if i % 2 == 0 and i % 3 == 0:
        stand += 1
    if (i-1) % 2 == 0 and (i-1) % 3 == 0:
        dp.append(dp[i-1] + (stand-1))
    else:
        dp.append(dp[i-1]+stand)
    
for _ in range(T):
    print(dp[int(input())])


